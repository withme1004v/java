package study;

public class study0119_05_제어문_if_else_if {

	public static void main(String[] args) {
		int num = 3;
		if( num ==1 )
		{
			System.out.println("숫자1입니다.");
		}
		else if( num ==2 ) 
		{
			System.out.println("숫자2입니다.");
		}
		else if( num ==3 ) 
		{
			System.out.println("숫자3입니다.");
		}
		else
		{
			System.out.println("숫자 1~3을 벗어났습니다.");
		}

	}

}
