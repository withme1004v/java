package study;

public class study0119_비교관계연산자 {

	public static void main(String[] args) {
		//비교연산
		int su1 = 10;
		int su2 = 5;
		System.out.println(su1 > su2);
		System.out.println(su1 < su2);
		System.out.println(su1 == su2);
		System.out.println(su1 != su2);
		
		//논리연산
		System.out.println(su1 > su2 && su1 < su2);
		System.out.println(su1 > su2 || su1 < su2);
	}

}
